#include <unistd.h>

int main(int ac, char **av)
{
	int i;
	int j;
	int k;

	i = 0;
	if (ac == 3)
	{
		while (av[1][i])
		{
			k = 0;
			while (av[1][i] != av[1][k] && k < i)
				k++;
			if (k != i)
				i++;
			else
			{
				j = 0;
				while (av[2][j] && av[2][j] != av[1][i])
					j++;
				if (av[2][j] == av[1][i])
					write(1, &(av[1][i]), 1);
				i++;
			}
		}
	}
	write(1, "\n", 1);
}
